void fill_field(int fieldLength, char field[HEIGHT][MAX], char template[HEIGHT][WIDTH])
{
    for (int i = 0; i < HEIGHT; i++) {
        for (int j = fieldLength; j < fieldLength + 8; j++) {
            field[i][j] = template[i][j - fieldLength];
        }
    }
}

